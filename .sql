--  DDL for View TEAM_PERFORMANCE_REPORT
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "IS680_JANARDHANAN"."TEAM_PERFORMANCE_REPORT" ("TOURNAMENT_NAME", "TEAM_NAME", "MATCHES_PLAYED", "WINS", "LOSSES", "DRAWS", "CANCELLED_MATCHES", "POINTS", "GOALS_SCORED", "GOALS_CONCEDED", "STANDING") AS 
  WITH TEAM_MATCHES AS (
    SELECT 
        M.MATCH_ID,
        M.TOURNAMENT_ID,
        T.TOURNAMENT_NAME,
        M.WINNER,
        M.STATUS_ID,
        M.HOME_ID AS TEAM_ID,
        TM.TEAM_NAME,
        'HOME' AS SIDE
    FROM MATCHES M
    JOIN TOURNAMENT T ON T.TOURNAMENT_ID = M.TOURNAMENT_ID
    JOIN TEAMS TM ON TM.TEAM_ID = M.HOME_ID

    UNION ALL

    SELECT 
        M.MATCH_ID,
        M.TOURNAMENT_ID,
        T.TOURNAMENT_NAME,
        M.WINNER,
        M.STATUS_ID,
        M.AWAY_ID AS TEAM_ID,
        TM.TEAM_NAME,
        'AWAY' AS SIDE
    FROM MATCHES M
    JOIN TOURNAMENT T ON T.TOURNAMENT_ID = M.TOURNAMENT_ID
    JOIN TEAMS TM ON TM.TEAM_ID = M.AWAY_ID
),

-- Exclude penalties
GOALS AS (
    SELECT 
        PG.MATCH_ID,
        PG.TEAM_ID,
        COUNT(*) AS GOALS_SCORED
    FROM PLAYER_GOAL PG
    WHERE PG.IS_PENALTY = 'N'
    GROUP BY PG.MATCH_ID, PG.TEAM_ID
),

TEAM_STATS AS (
    SELECT 
        TM.TOURNAMENT_NAME,
        TM.TEAM_NAME,

        COUNT(CASE WHEN WINNER <> 'CANCELLED' THEN 1 END) AS MATCHES_PLAYED,

        SUM(CASE WHEN WINNER = TM.TEAM_NAME THEN 1 ELSE 0 END) AS WINS,

        SUM(CASE 
            WHEN WINNER NOT IN ('Draw', 'CANCELLED') AND WINNER <> TM.TEAM_NAME THEN 1 
            ELSE 0 
        END) AS LOSSES,

        SUM(CASE 
            WHEN WINNER = 'Draw' THEN 1 
            ELSE 0 
        END) AS DRAWS,

        SUM(CASE 
            WHEN WINNER = 'CANCELLED' THEN 1 
            ELSE 0 
        END) AS CANCELLED_MATCHES,

        SUM(CASE 
            WHEN WINNER = TM.TEAM_NAME THEN 3
            WHEN WINNER = 'Draw' THEN 1
            ELSE 0 
        END) AS POINTS,

        -- Goals Scored (no penalties)
        SUM(NVL(GS.GOALS_SCORED, 0)) AS GOALS_SCORED,

        -- Goals Conceded (no penalties)
        SUM(NVL(GC.GOALS_SCORED, 0)) AS GOALS_CONCEDED

    FROM TEAM_MATCHES TM
    LEFT JOIN GOALS GS ON TM.MATCH_ID = GS.MATCH_ID AND TM.TEAM_ID = GS.TEAM_ID
    LEFT JOIN GOALS GC ON TM.MATCH_ID = GC.MATCH_ID AND TM.TEAM_ID <> GC.TEAM_ID
    GROUP BY TM.TOURNAMENT_NAME, TM.TEAM_NAME
),

FINAL AS (
    SELECT 
        TS.*,
        RANK() OVER (PARTITION BY TOURNAMENT_NAME ORDER BY POINTS DESC, GOALS_SCORED - GOALS_CONCEDED DESC) AS STANDING
    FROM TEAM_STATS TS
)

SELECT "TOURNAMENT_NAME","TEAM_NAME","MATCHES_PLAYED","WINS","LOSSES","DRAWS","CANCELLED_MATCHES","POINTS","GOALS_SCORED","GOALS_CONCEDED","STANDING" FROM FINAL
ORDER BY TOURNAMENT_NAME, STANDING
;
