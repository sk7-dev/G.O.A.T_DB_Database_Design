  CREATE OR REPLACE EDITIONABLE TRIGGER "IS680_JANARDHANAN"."UPDATE_TEAM_STANDINGS" 
AFTER INSERT OR UPDATE ON MATCHES
FOR EACH ROW
DECLARE
BEGIN
  -- Use a PL/SQL block to update standings based on points in descending order
  FOR grp IN (
    SELECT DISTINCT GROUP_ID, TOURNAMENT_ID
    FROM TOURNEY_GROUP_TEAM
  ) LOOP
    FOR rec IN (
      SELECT TEAM_ID
      FROM TOURNEY_GROUP_TEAM
      WHERE GROUP_ID = grp.GROUP_ID AND TOURNAMENT_ID = grp.TOURNAMENT_ID
      ORDER BY NVL(POINTS, 0) DESC
    ) LOOP
      UPDATE TOURNEY_GROUP_TEAM
      SET STANDING = (
        SELECT COUNT(*) + 1
        FROM TOURNEY_GROUP_TEAM t2
        WHERE t2.GROUP_ID = grp.GROUP_ID
          AND t2.TOURNAMENT_ID = grp.TOURNAMENT_ID
          AND NVL(t2.POINTS, 0) > NVL(
            (SELECT POINTS FROM TOURNEY_GROUP_TEAM
             WHERE TEAM_ID = rec.TEAM_ID
               AND GROUP_ID = grp.GROUP_ID
               AND TOURNAMENT_ID = grp.TOURNAMENT_ID), 0)
      )
      WHERE TEAM_ID = rec.TEAM_ID
        AND GROUP_ID = grp.GROUP_ID
        AND TOURNAMENT_ID = grp.TOURNAMENT_ID;
    END LOOP;
  END LOOP;
END;
