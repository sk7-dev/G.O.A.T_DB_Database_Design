--  DDL for Trigger UPDATE_TEAM_POINTS_U
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE TRIGGER "IS680_JANARDHANAN"."UPDATE_TEAM_POINTS_U" 
AFTER UPDATE ON MATCHES
FOR EACH ROW
DECLARE
  old_home_pts INT := 0;
  old_away_pts INT := 0;
  new_home_pts INT := 0;
  new_away_pts INT := 0;
BEGIN
  IF :NEW.STATUS_ID = 1 AND :NEW.IS_PLAYOFF = 'N' AND (
      :NEW.HOME_SCORE != :OLD.HOME_SCORE OR 
      :NEW.AWAY_SCORE != :OLD.AWAY_SCORE OR 
      :NEW.STATUS_ID != :OLD.STATUS_ID OR
      :NEW.IS_PLAYOFF != :OLD.IS_PLAYOFF
  ) THEN

    IF :OLD.STATUS_ID = 1 AND :OLD.IS_PLAYOFF = 'N' THEN
      IF :OLD.HOME_SCORE > :OLD.AWAY_SCORE THEN
        old_home_pts := 3;
      ELSIF :OLD.HOME_SCORE < :OLD.AWAY_SCORE THEN
        old_away_pts := 3;
      ELSE
        old_home_pts := 1;
        old_away_pts := 1;
      END IF;
    END IF;

    IF :NEW.HOME_SCORE > :NEW.AWAY_SCORE THEN
      new_home_pts := 3;
    ELSIF :NEW.HOME_SCORE < :NEW.AWAY_SCORE THEN
      new_away_pts := 3;
    ELSE
      new_home_pts := 1;
      new_away_pts := 1;
    END IF;

    -- Revert old points
    UPDATE TOURNEY_GROUP_TEAM
    SET POINTS = NVL(POINTS, 0) - old_home_pts
    WHERE TEAM_ID = :OLD.HOME_ID AND GROUP_ID = :NEW.GROUP_ID AND TOURNAMENT_ID = :NEW.TOURNAMENT_ID;

    UPDATE TOURNEY_GROUP_TEAM
    SET POINTS = NVL(POINTS, 0) - old_away_pts
    WHERE TEAM_ID = :OLD.AWAY_ID AND GROUP_ID = :NEW.GROUP_ID AND TOURNAMENT_ID = :NEW.TOURNAMENT_ID;

    -- Add new points
    UPDATE TOURNEY_GROUP_TEAM
    SET POINTS = NVL(POINTS, 0) + new_home_pts
    WHERE TEAM_ID = :NEW.HOME_ID AND GROUP_ID = :NEW.GROUP_ID AND TOURNAMENT_ID = :NEW.TOURNAMENT_ID;

    UPDATE TOURNEY_GROUP_TEAM
    SET POINTS = NVL(POINTS, 0) + new_away_pts
    WHERE TEAM_ID = :NEW.AWAY_ID AND GROUP_ID = :NEW.GROUP_ID AND TOURNAMENT_ID = :NEW.TOURNAMENT_ID;
  END IF;
END;
