  CREATE OR REPLACE FORCE EDITIONABLE VIEW "IS680_JANARDHANAN"."TOURNAMENT_SUMMARY_REPORT" ("TOURNAMENT_ID", "TOURNAMENT_NAME", "START_DATE", "END_DATE", "NUM_TEAMS", "NUM_MATCHES", "TOTAL_GOALS") AS 
  SELECT 
    T.TOURNAMENT_ID,
    T.TOURNAMENT_NAME,
    T.START_DATE,
    T.END_DATE,

    -- Number of distinct teams in the tournament via GROUPS
    (
        SELECT COUNT(DISTINCT TG.TEAM_ID)
        FROM GROUPS G
        JOIN TOURNEY_GROUP_TEAM TG ON G.GROUP_ID = TG.GROUP_ID
        WHERE G.TOURNAMENT_ID = T.TOURNAMENT_ID
    ) AS NUM_TEAMS,

    -- Number of matches in the tournament
    (
        SELECT COUNT(*) 
        FROM MATCHES M 
        WHERE M.TOURNAMENT_ID = T.TOURNAMENT_ID
    ) AS NUM_MATCHES,

    -- Total goals scored in the tournament
    (
        SELECT COUNT(*) 
        FROM PLAYER_GOAL PG 
        JOIN MATCHES M ON PG.MATCH_ID = M.MATCH_ID
        WHERE M.TOURNAMENT_ID = T.TOURNAMENT_ID
    ) AS TOTAL_GOALS

FROM 
    TOURNAMENT T
ORDER BY 
    T.START_DATE
;
