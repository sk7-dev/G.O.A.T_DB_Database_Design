  CREATE OR REPLACE FORCE EDITIONABLE VIEW "IS680_JANARDHANAN"."TOP_10_SCORERS" ("PLAYER_NAME", "TEAM_NAME", "PENALTY_GOALS", "OWN_GOALS", "TOTAL_GOALS") AS 
  SELECT 
    P.PLAYER_NAME,
    T.TEAM_NAME,
    COUNT(CASE WHEN PG.IS_PENALTY = 'Y' THEN 1 END) AS PENALTY_GOALS,
    COUNT(CASE WHEN PG.IS_OWN_GOAL = 'Y' THEN 1 END) AS OWN_GOALS,
    COUNT(*) AS TOTAL_GOALS
FROM PLAYER_GOAL PG
JOIN PLAYER P ON PG.PLAYER_ID = P.PLAYER_ID
JOIN TEAMS T ON PG.TEAM_ID = T.TEAM_ID
GROUP BY P.PLAYER_NAME, T.TEAM_NAME
ORDER BY TOTAL_GOALS DESC
FETCH FIRST 10 ROWS ONLY
;
