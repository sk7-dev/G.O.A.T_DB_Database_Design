  CREATE OR REPLACE EDITIONABLE TRIGGER "IS680_JANARDHANAN"."SET_MATCH_WINNER" 
BEFORE INSERT OR UPDATE ON MATCHES
FOR EACH ROW
DECLARE
    HOME_TEAM_NAME TEAMS.TEAM_NAME%TYPE;
    AWAY_TEAM_NAME TEAMS.TEAM_NAME%TYPE;
BEGIN
    -- Get team names
    SELECT TEAM_NAME INTO HOME_TEAM_NAME FROM TEAMS WHERE TEAM_ID = :NEW.HOME_ID;
    SELECT TEAM_NAME INTO AWAY_TEAM_NAME FROM TEAMS WHERE TEAM_ID = :NEW.AWAY_ID;

    -- Cancelled match
    IF :NEW.STATUS_ID = 3 THEN  -- Cancelled
        :NEW.WINNER := 'No Result';  -- Set No Result in case of cancellation

    -- Completed match
    ELSIF :NEW.STATUS_ID = 1 THEN  -- Completed
        IF :NEW.HOME_SCORE > :NEW.AWAY_SCORE THEN
            :NEW.WINNER := HOME_TEAM_NAME;  -- HOME team wins

        ELSIF :NEW.HOME_SCORE < :NEW.AWAY_SCORE THEN
            :NEW.WINNER := AWAY_TEAM_NAME;  -- AWAY team wins

        ELSIF :NEW.HOME_SCORE = :NEW.AWAY_SCORE THEN
            -- Check penalty if scores are tied
            IF :NEW.HOME_PENALTY IS NOT NULL AND :NEW.AWAY_PENALTY IS NOT NULL THEN
                IF :NEW.HOME_PENALTY > :NEW.AWAY_PENALTY THEN
                    :NEW.WINNER := HOME_TEAM_NAME;  -- HOME team wins based on penalties
                ELSIF :NEW.HOME_PENALTY < :NEW.AWAY_PENALTY THEN
                    :NEW.WINNER := AWAY_TEAM_NAME;  -- AWAY team wins based on penalties
                ELSE
                    :NEW.WINNER := 'Draw';  -- Draw after penalties
                END IF;
            ELSE
                :NEW.WINNER := 'Draw';  -- Draw, no penalty
            END IF;
        END IF;
    END IF;
EXCEPTION
    WHEN OTHERS THEN
        -- Handle exceptions, e.g., log them or raise an application error
        RAISE_APPLICATION_ERROR(-20001, 'Error in SET_MATCH_WINNER trigger: ' || SQLERRM);
END SET_MATCH_WINNER;
/
